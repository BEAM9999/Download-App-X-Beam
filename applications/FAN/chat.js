// ─── 11. Send Message ────────────────────────────────────
function handleSend() {
  // ถ้า AI กำลังตอบ → กดหยุด
  if (isGenerating) {
    if (abortCtrl) abortCtrl.abort();
    sfxAbort();
    return;
  }
  const input = $('msgInput');
  const text = input.value.trim();
  if (!text) return;

  // ปิดไมค์อัตโนมัติเมื่อส่งข้อความ
  if (isRecording) stopListening();

  sfxSend();

  lastUserTs = Date.now();
  resetIdleTimer();
  input.value = '';
  draft = '';
  save();
  autoResize();

  // Handle mood: if sulking, check for coax
  if (mood === 'sulking' || mood === 'angry') {
    const isCoax = COAX_WORDS.some(w => text.includes(w));
    if (isCoax) {
      coaxCount++;
      if (coaxCount >= coaxNeeded) {
        mood = 'normal';
        coaxCount = 0;
        coaxNeeded = 3 + Math.floor(Math.random() * 3);
      }
    }
    save();
  }
  // If mood was 'annoyed', user response resets to normal
  if (mood === 'asking1' || mood === 'asking2' || mood === 'annoyed') {
    mood = 'normal';
    save();
  }

  // Ensure session exists
  let session = activeSession();
  if (!session) {
    session = createSession();
  }

  session.messages.push({ role: 'user', content: text, ts: Date.now() });
  renderMessages();
  save();

  // Auto-title after first message
  if (session.messages.filter(m => m.role === 'user').length === 1) {
    autoTitle(session);
  }

  // Hide welcome
  const welcome = $('welcomeArea');
  if (welcome) welcome.remove();

  sendToAI(session);
}

