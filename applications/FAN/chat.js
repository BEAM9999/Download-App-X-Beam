  $('btnOpenSettings').addEventListener('click', () => { sfxBtn(); openSettings(); });

  // Model panel
  $('btnModelClose').addEventListener('click', () => { sfxBtn(); closePanel('modelOverlay'); });
  $('modelOverlay').addEventListener('click', e => { if (e.target === e.currentTarget) { sfxBtn(); closePanel('modelOverlay'); } });
  $('btnOpenPastePlatformConfig').addEventListener('click', () => { sfxBtn(); togglePlatformPasteBox(); });
