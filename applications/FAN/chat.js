// ── Timer / Stopwatch System ──
let timerIntervals = {}; // { widgetId: { intervalId, remaining, total } }

function createTimerWidget(session, durationSec, label) {
  const msgIdx = Math.max(0, session.messages.length - 1);
  const data = { duration: durationSec, remaining: durationSec, label: label || `จับเวลา ${formatDuration(durationSec)}`, running: true };
  const widget = addWidget(session, 'timer', data, msgIdx);
  startTimer(widget);
  return widget;
}

function formatDuration(sec) {
  const m = Math.floor(sec / 60);
  const s = sec % 60;
  return `${String(m).padStart(2,'0')}:${String(s).padStart(2,'0')}`;
}

function startTimer(widget) {
  if (!widget) return;
  if (timerIntervals[widget.id]) clearInterval(timerIntervals[widget.id].intervalId);
  timerIntervals[widget.id] = {
    intervalId: setInterval(() => {
      widget.data.remaining--;
      if (widget.data.remaining <= 0) {
        widget.data.remaining = 0;
        widget.data.running = false;
        clearInterval(timerIntervals[widget.id].intervalId);
        delete timerIntervals[widget.id];
        playAlarmSound(); // Reuse alarm sound
        sendNotification('⏱️ จับเวลาเสร็จแย้ว! ' + widget.data.label);
        const session = activeSession();
        if (session) {
          session.messages.push({ role: 'assistant', content: `⏱️ **หมดเวลาแย้ว!** ${widget.data.label}`, ts: Date.now() });
        }
        save();
      }
      // Update the timer display if visible
      const el = document.querySelector(`[data-widget-id="${widget.id}"] .timer-display`);
      if (el) el.textContent = formatDuration(widget.data.remaining);
      const progressEl = document.querySelector(`[data-widget-id="${widget.id}"] .timer-progress`);
      if (progressEl) {
        const pct = widget.data.duration > 0 ? ((widget.data.duration - widget.data.remaining) / widget.data.duration) * 100 : 100;
        progressEl.style.width = pct + '%';
      }
    }, 1000)
  };
}

function toggleTimer(widgetId) {
  const session = activeSession();
  if (!session) return;
  const w = session.widgets?.find(w => w.id === widgetId);
  if (!w) return;
  if (w.data.running) {
    w.data.running = false;
    if (timerIntervals[w.id]) { clearInterval(timerIntervals[w.id].intervalId); delete timerIntervals[w.id]; }
  } else {
    if (w.data.remaining <= 0) w.data.remaining = w.data.duration;
    w.data.running = true;
    startTimer(w);
  }
  save();
  renderMessages();
}

function deleteTimerWidget(widgetId) {
  if (timerIntervals[widgetId]) { clearInterval(timerIntervals[widgetId].intervalId); delete timerIntervals[widgetId]; }
  const session = activeSession();
  if (session) removeWidget(session, widgetId);
  renderMessages();
}

