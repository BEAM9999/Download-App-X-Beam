function resetIdleTimer() {
  clearTimeout(idleTimer);
  lastUserTs = Date.now();
  idleAttemptCount = 0;
  idleGaveUp = false;

  if (mood === 'sulking' || mood === 'angry') return;

  scheduleNextIdle();
}

function scheduleNextIdle() {
  clearTimeout(idleTimer);
  const session = activeSession();
  if (!session || !session.messages.length) return;
  if (idleGaveUp) return;
  if (mood === 'sulking') return;

  // Smart delay based on context
  let delay;
  if (idleAttemptCount === 0) {
    delay = isLikelySleeping() ? IDLE_SLEEP_QUIET : IDLE_FIRST_CHECK;
  } else {
    // Increase delay progressively: 3min, 5min, 8min, 10min…
    const progressiveDelay = IDLE_FOLLOWUP + (idleAttemptCount * 120000);
    delay = isLikelySleeping() ? IDLE_SLEEP_QUIET : Math.min(progressiveDelay, 600000);
  }

  idleTimer = setTimeout(() => sendAiIdleMessage(), delay);
}

