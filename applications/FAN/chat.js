// ── Thai timezone helper ──
function getThaiTime() {
  try { return new Date(new Date().toLocaleString('en-US', { timeZone: 'Asia/Bangkok' })); }
  catch { return new Date(); }
}
function getThaiHour() { return getThaiTime().getHours(); }
function getThaiMinute() { return getThaiTime().getMinutes(); }

function isLikelySleeping() {
  const h = getThaiHour();
  return h >= 23 || h < 7;
}
function isLikelyMealTime() {
  const h = getThaiHour();
  return (h >= 7 && h <= 8) || (h >= 11 && h <= 13) || (h >= 17 && h <= 19);
}
function isLateNight() {
  const h = getThaiHour();
  return h >= 22 || h < 6;
}

// ── Build context-aware idle prompt for AI ──
function buildIdleContext() {
  const now = getThaiTime();
  const h = now.getHours();
  const m = now.getMinutes();
  const idleMs = Date.now() - lastUserTs;
  const idleMin = Math.floor(idleMs / 60000);
  const idleSec = Math.floor(idleMs / 1000);
  const timeStr = `${String(h).padStart(2,'0')}:${String(m).padStart(2,'0')}`;

  let situation = '';
  if (isLikelySleeping()) {
    situation = 'แฟนอาจจะหลับแล้ว (เวลาไทย ' + timeStr + ' น.) → อย่าทักกวน ส่งข้อความอ่อนหวานทิ้งไว้ให้ตื่นมาอ่านก็ได้ หรือแค่ "ราตรีสวัสดิ์" สั้นๆ';
  } else if (isLikelyMealTime()) {
    situation = 'อาจจะกินข้าวอยู่ (เวลา ' + timeStr + ' น.) → ทักเบาๆ เช่น กินข้าวรึยังงับ';
  } else if (idleMin < 3) {
    situation = 'เพิ่งหายไปไม่นาน อาจจะไปห้องน้ำ/ทำธุระแป๊ป → ทักสั้นๆ เบาๆ';
  } else if (idleMin < 10) {
    situation = 'หายไปสักพัก (' + idleMin + ' นาที) อาจจะยุ่งอยู่ → ทักด้วยความคิดถึง/เหงา';
  } else if (idleMin < 30) {
    situation = 'หายไปนาน (' + idleMin + ' นาที) อาจจะยุ่งจริงๆ → ทักสั้นๆ แค่บอกว่ารอ/คิดถึง';
  } else {
    situation = 'หายไปนานมาก (' + idleMin + ' นาที) → ส่งข้อความสั้นๆทิ้งไว้ ไม่กวนเยอะ';
  }

  // Mood escalation based on attempt count
  let moodHint = '';
  if (idleAttemptCount === 0) {
    moodHint = 'ทักครั้งแรก → เป็นมิตร สดใส น่ารัก';
  } else if (idleAttemptCount === 1) {
    moodHint = 'ทักครั้งที่ 2 → ยังน่ารักแต่เริ่มอยากคุย';
  } else if (idleAttemptCount === 2) {
    moodHint = 'ทักครั้งที่ 3 → เริ่มน้อยใจนิดๆ ทำไมไม่ตอบ';
  } else if (idleAttemptCount === 3) {
    moodHint = 'ทักครั้งที่ 4 → งอนเบาๆ แต่ยังรัก';
  } else {
    moodHint = 'ทักครั้งสุดท้าย → เริ่มยอมแพ้ ส่งข้อความทิ้งไว้ แล้วจะไม่ทักอีกจนกว่าแฟนจะตอบ';
  }

  return `[ระบบ IDLE: แฟนไม่ตอบมา ${idleMin > 0 ? idleMin + ' นาที' : idleSec + ' วินาที'} แล้ว]
[เวลาไทยตอนนี้: ${timeStr} น.]
[สถานการณ์: ${situation}]
[อารมณ์ที่ควรแสดง: ${moodHint}]
[ทักไปแล้ว ${idleAttemptCount} ครั้ง]
[ข้อสำคัญ: ส่งข้อความ 1-2 ประโยค สั้นๆ เป็นธรรมชาติ ไม่ซ้ำกับข้อความก่อนหน้า ห้ามพูดว่า "ตามระบบ/ตามโค้ด/idle" พูดเหมือนคนจริงๆ]`;
}

// ── Request idle AI message (lightweight, no streaming) ──
async function requestIdleAiMessage(session) {
  const pe = PERSONAS[persona];
  if (!pe) return null;

  // Build minimal message history (last 6 messages for context)
  const recentMsgs = session.messages.slice(-6);
  const idleCtx = buildIdleContext();

  // Try Gemini first, then OpenRouter
  if (getGeminiKeySlots().length) {
    try {
      const route = await findGeminiRoute(getSelectedModelId('gemini'), { allowOtherModels: true });
      if (route) {
        const contents = recentMsgs.map(m => ({
          role: m.role === 'user' ? 'user' : 'model',
          parts: [{ text: m.content }]
        }));
        // Ensure contents alternate properly and start with user
        if (!contents.length || contents[0].role !== 'user') {
          contents.unshift({ role: 'user', parts: [{ text: '...' }] });
        }
        // Ensure last message is from user (Gemini requires it)
        if (contents[contents.length - 1].role !== 'user') {
          contents.push({ role: 'user', parts: [{ text: '...' }] });
        }

        const payload = {
          contents,
          systemInstruction: { parts: [{ text: resolvePersonaPrompt(pe, persona) + '\n\n' + idleCtx }] },
          generationConfig: { maxOutputTokens: 200, temperature: 1.0, topP: 0.95 }
        };
        const res = await fetch(
          `${GEMINI_BASE_URL}/${encodeURIComponent(route.modelId)}:generateContent?key=${encodeURIComponent(route.key)}`,
          { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(payload) }
        );
        if (res.ok) {
          const json = await res.json();
          const text = extractGeminiText(json);
          if (text) return text;
        }
      }
    } catch (_) {}
  }

  // Try OpenRouter as fallback
  const orModels = getOrderedOpenRouterModels(getSelectedModelId('openrouter'), new Set());
  for (const model of orModels) {
    if (!model.key || !model.modelId) continue;
    try {
      const messages = [
        { role: 'system', content: resolvePersonaPrompt(pe, persona) + '\n\n' + idleCtx },
        ...recentMsgs.map(m => ({ role: m.role === 'user' ? 'user' : 'assistant', content: m.content }))
      ];
      const res = await fetch(OPENROUTER_URL, {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${model.key}`,
          'Content-Type': 'application/json',
          'HTTP-Referer': location.href,
          'X-Title': 'FAN Chat Idle'
        },
        body: JSON.stringify({ model: model.modelId, messages, max_tokens: 200, temperature: 1.0, stream: false })
      });
      if (res.ok) {
        const json = await res.json();
        const text = json?.choices?.[0]?.message?.content?.trim();
        if (text) return text;
      }
    } catch (_) {}
  }

  return null;
}

// ── Send AI-generated idle message ──
async function sendAiIdleMessage() {
  const session = activeSession();
  if (!session || !session.messages.length) return;
  if (isGenerating) return;
  if (idleGaveUp) return;

  // Don't send if user has been typing recently (check lastUserTs)
  const idleMs = Date.now() - lastUserTs;
  if (idleMs < 30000) return; // User was active in the last 30s

  // If sleeping and already sent a message, don't spam
  if (isLikelySleeping() && idleAttemptCount >= 1) {
    idleGaveUp = true;
    return;
  }

  // Max attempts reached
  if (idleAttemptCount >= IDLE_MAX_ATTEMPTS) {
    idleGaveUp = true;
    // Update mood
    if (mood !== 'sulking') {
      mood = idleAttemptCount >= 4 ? 'sulking' : 'angry';
      save();
    }
    return;
  }

  // Update mood progression based on attempt count
  if (idleAttemptCount === 0) mood = 'asking1';
  else if (idleAttemptCount === 1) mood = 'asking2';
  else if (idleAttemptCount === 2) mood = 'annoyed';
  else if (idleAttemptCount === 3) mood = 'angry';
  else mood = 'sulking';
  save();

  // Get AI-generated message
  const aiText = await requestIdleAiMessage(session);
  if (aiText) {
    const sanitized = sanitizeAiBotText(aiText);
    session.messages.push({ role: 'assistant', content: sanitized, ts: Date.now() });
    renderMessages();
    save();
    sfxAiDone();
    sendNotification(sanitized);
  }

  idleAttemptCount++;

  // Schedule next idle check
  if (idleAttemptCount < IDLE_MAX_ATTEMPTS && !idleGaveUp) {
    scheduleNextIdle();
  }
}

