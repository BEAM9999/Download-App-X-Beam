// ─── 14. STT (Speech-to-Text) ────────────────────────────
function initSTT() {
  const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
  if (!SpeechRecognition) return;
  recognition = new SpeechRecognition();
  recognition.lang = 'th-TH';
  recognition.continuous = true;
  recognition.interimResults = true;

  recognition.onresult = e => {
    let interim = '';
    for (let i = e.resultIndex; i < e.results.length; i++) {
      if (e.results[i].isFinal) {
        sttFinalText += e.results[i][0].transcript;
      } else {
        interim += e.results[i][0].transcript;
      }
    }
    const input = $('msgInput');
    input.value = sttFinalText + interim;
    autoResize();
    updateSendBtn();
  };

  recognition.onend = () => {
    if (isRecording) recognition.start();
  };
  recognition.onerror = () => { stopListening(); };
}

function startWaveAnimation() {
  if (!analyser) return;
  const bars = qsa('.vw-bar');
  const dataArr = new Uint8Array(analyser.frequencyBinCount);
  const step = Math.max(1, Math.floor(dataArr.length / bars.length));
  function frame() {
    if (!isRecording) { bars.forEach(b => b.style.height = '4px'); return; }
    analyser.getByteFrequencyData(dataArr);
    bars.forEach((b, i) => {
      const val = dataArr[i * step] / 255;
      b.style.height = Math.max(4, Math.round(val * 26)) + 'px';
    });
    waveAnim = requestAnimationFrame(frame);
  }
  waveAnim = requestAnimationFrame(frame);
}

