// ── Smart widget command detector ──
function detectWidgetCommand(text) {
  const t = text.toLowerCase();

  // YouTube (highest priority — concrete URL)
  const ytMatch = text.match(/(?:https?:\/\/)?(?:www\.)?(?:youtube\.com\/watch\?v=|youtu\.be\/)([\w-]{11})/i);
  if (ytMatch) return { type: 'youtube', videoId: ytMatch[1], query: null };

  // YouTube search intent: "เปิดเพลง X", "ดูคลิป X", "หาเพลง X", "youtube X", etc.
  const ytSearchRe = /(?:เปิดเพลง|ดูเพลง|เปิดคลิป|ดูคลิป|หาเพลง|ฟังเพลง|อยากดู|อยากฟัง|ค้นหาเพลง|ค้นหาคลิป|หาคลิป)\s*(.{2,})/i.exec(t)
    || /(?:youtube|yt)\s+(.{2,})/i.exec(t);
  if (ytSearchRe) return { type: 'youtube', videoId: null, query: ytSearchRe[1].replace(/["""''ๆ]/g, '').trim() };

  // Alarm
  const alarmKeyword = /นาฬิกาปลุก|ปลุก|alarm|แจ้งเตือน(?:ฉัน|ด้วย)?(?:เวลา|ตอน|at)?/i.test(t);
  // Exclude "แจ้งเตือน" used in generic context without time
  if (alarmKeyword) {
    const time = parseSmartTime(text);
    if (time) return { type: 'alarm', time };
  }

  // Timer / stopwatch
  if (/จับเวลา|ตั้งเวลา|timer|stopwatch|นับถอยหลัง|countdown|ตั้งนับ|นับเวลา/.test(t)) {
    const dur = parseSmartDuration(text);
    return { type: 'timer', duration: isNaN(dur) ? 300 : dur };
  }

  // Calculator
  if (/เครื่องคิดเลข|calculator|คำนวณ|เปิดคิดเลข|คิดเลข/.test(t)) {
    return { type: 'calc' };
  }

  // Map
  if (/google\s*map|แผนที่|เปิดแผนที่|ตำแหน่ง|location(?!\s*\w)|map(?!\w)/.test(t)) {
    // Extract location query: everything after map keywords
    const loc = text
      .replace(/(?:ใน|ที่|of|at|near|around|ของ)\s*/gi, '')
      .replace(/(?:google\s*map|แผนที่|เปิดแผนที่|ตำแหน่ง|location|เปิด|ให้|หน่อย|map)/gi, '')
      .replace(/\s+/g, ' ').trim();
    return { type: 'map', query: loc || '' };
  }

  return null;
}

