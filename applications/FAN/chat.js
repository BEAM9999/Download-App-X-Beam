function sfxKey() {
  // เสียงคีย์บอร์ดพิมพ์ — เสียง click เบาๆ
  if (window._fanPlaySoundFX && window._fanPlaySoundFX('type')) return;
  try {
    const ac = _ac(), t = ac.currentTime;
    const buf = ac.createBuffer(1, ac.sampleRate * 0.04, ac.sampleRate);
    const d = buf.getChannelData(0);
    for (let i = 0; i < d.length; i++) d[i] = (Math.random() * 2 - 1) * Math.exp(-i / (d.length * 0.12));
    const src = ac.createBufferSource();
    src.buffer = buf;
    const g = ac.createGain();
    g.gain.setValueAtTime(0.18, t);
    g.gain.exponentialRampToValueAtTime(0.001, t + 0.04);
    const bq = ac.createBiquadFilter();
    bq.type = 'bandpass'; bq.frequency.value = 3200; bq.Q.value = 1.2;
    src.connect(bq); bq.connect(g); g.connect(ac.destination);
    src.start(t); src.stop(t + 0.04);
  } catch (_) {}
}

