function orderGeminiKeySlots(preferredKeyId = geminiActiveKeyId) {
  const slots = getGeminiKeySlots();
  if (!preferredKeyId) return slots;
  return slots.slice().sort((left, right) => {
    if (left.id === preferredKeyId) return -1;
    if (right.id === preferredKeyId) return 1;
    return left.index - right.index;
  });
}

function getGeminiModelAggregateStatus(modelId) {
  const slots = getGeminiKeySlots();
  let hasOk = false;
  let hasFresh = false;
  let hasLoading = false;
  for (const slot of slots) {
    const record = getGeminiModelRecord(slot.id, modelId);
    if (!record) continue;
    if (record.status === 'loading') hasLoading = true;
    if (!isGeminiRecordFresh(record)) continue;
    hasFresh = true;
    if (record.status === 'ok') hasOk = true;
  }
  if (hasOk) return 'ok';
  if (hasLoading) return 'loading';
  return hasFresh ? 'err' : 'loading';
}

function hasGeminiFreshData(modelId) {
  return getGeminiKeySlots().some(slot => isGeminiRecordFresh(getGeminiModelRecord(slot.id, modelId)));
}

function getGeminiRouteFromCache(preferredModelId, excludePairs = new Set()) {
  for (const slot of orderGeminiKeySlots()) {
    const pairKey = `${slot.id}::${preferredModelId}`;
    if (excludePairs.has(pairKey)) continue;
    const record = getGeminiModelRecord(slot.id, preferredModelId);
    if (isGeminiRecordFresh(record) && record.status === 'ok') {
      return { modelId: preferredModelId, keySlotId: slot.id, key: slot.key };
    }
  }
  return null;
}

