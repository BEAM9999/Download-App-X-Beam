// ── Extract AI widget action tags from response, return cleaned text + actions ──
// AI embeds <open:youtube>query</open:youtube>, <open:ytmusic>query</open:ytmusic>, <open:maps>query</open:maps>
function extractWidgetActions(text) {
  const actions = [];
  let clean = text;

  clean = clean.replace(/<open:youtube>([\s\S]*?)<\/open:youtube>/gi, (_, q) => {
    actions.push({ type: 'youtube', query: q.trim() });
    return '';
  });
  clean = clean.replace(/<open:ytmusic>([\s\S]*?)<\/open:ytmusic>/gi, (_, q) => {
    actions.push({ type: 'ytmusic', query: q.trim() });
    return '';
  });
  clean = clean.replace(/<open:maps>([\s\S]*?)<\/open:maps>/gi, (_, q) => {
    actions.push({ type: 'maps', query: q.trim() });
    return '';
  });

  // Also catch unclosed / partial tags as fallback
  clean = clean.replace(/<open:(youtube|ytmusic|maps)[^>]*>/gi, '');
  clean = clean.replace(/<\/open:(youtube|ytmusic|maps)>/gi, '');

  return { text: clean.trim(), actions };
}

// ── Fire widget actions: create widget + auto-open URL ──
// Safety: reject actions where the query looks like a question (AI hallucinated a tag)
function isQueryLooksLikeQuestion(q) {
  if (!q) return true;
  if (q.trim().length < 2) return true;
  // Contains question mark or common Thai question words at start
  if (/[?？]/.test(q)) return true;
  if (/^(?:อะไร|ที่ไหน|ยังไง|อย่างไร|ทำไม|เมื่อไ|ใคร|ไหน|มั้ย|มาก|หรือ|แล้ว|ดีมั้ย|ดีไหม|what|where|why|how|who|which)\b/i.test(q.trim())) return true;
  return false;
}

function fireWidgetActions(actions, session) {
  if (!actions.length) return;
  for (const action of actions) {
    if (isQueryLooksLikeQuestion(action.query)) continue; // skip hallucinated tags
    if (action.type === 'youtube') {
      createYouTubeWidget(session, null, action.query);
      const url = 'https://www.youtube.com/results?search_query=' + encodeURIComponent(action.query);
      window.open(url, '_blank', 'noopener,noreferrer');
    } else if (action.type === 'ytmusic') {
      createYouTubeWidget(session, null, action.query);
      const url = 'https://music.youtube.com/search?q=' + encodeURIComponent(action.query);
      window.open(url, '_blank', 'noopener,noreferrer');
    } else if (action.type === 'maps') {
      createMapWidget(session, action.query);
      const url = 'https://www.google.com/maps/search/' + encodeURIComponent(action.query);
      window.open(url, '_blank', 'noopener,noreferrer');
    }
  }
  renderMessages();
}

// ── Process message for widget commands (called after AI responds) ──
function processWidgetCommands(userText, session) {
  if (!session) return;
  const cmd = detectWidgetCommand(userText);
  if (!cmd) return;

  const keepOld = wantsKeepOld(userText);
  const doReplace = !keepOld; // default: replace existing same-type widget

  // Helper: remove all existing widgets of a given type (cleans up timers/alarms too)
  function clearWidgetsOfType(type) {
    if (!session.widgets) return;
    const toRemove = session.widgets.filter(w => w.type === type);
    for (const w of toRemove) {
      if (alarmIntervals[w.id]) { clearInterval(alarmIntervals[w.id]); delete alarmIntervals[w.id]; }
      if (timerIntervals[w.id]) { clearInterval(timerIntervals[w.id].intervalId); delete timerIntervals[w.id]; }
    }
    session.widgets = session.widgets.filter(w => w.type !== type);
  }

  switch (cmd.type) {
    case 'alarm': {
      if (doReplace) clearWidgetsOfType('alarm');
      const t = cmd.time;
      createAlarmWidget(session, t.hours, t.minutes);
      break;
    }
    case 'timer': {
      if (doReplace) clearWidgetsOfType('timer');
      createTimerWidget(session, cmd.duration);
      break;
    }
    case 'calc': {
      // Calculator: if already open and asked again without keepOld, replace
      if (doReplace) clearWidgetsOfType('calc');
      createCalcWidget(session);
      break;
    }
    case 'map': {
      // Maps are handled via AI response tags — skip from user-message parsing
      break;
    }
    case 'youtube': {
      // YouTube is handled via AI response tags — skip from user-message parsing
      break;
    }
  }
  renderMessages();
}

