// ── Extract AI widget action tags from response, return cleaned text + actions ──
// Supported: youtube, ytmusic, maps, google, facebook, instagram, tiktok, twitter, shopee, lazada, spotify, netflix, line, messenger, discord, telegram, whatsapp, grab, foodpanda, robinhood, game, steam, playstore, appstore, url, app (catch-all for any app/site)
const OPEN_TAG_TYPES = ['youtube','ytmusic','maps','google','facebook','instagram','tiktok','twitter','shopee','lazada','spotify','netflix','line','messenger','discord','telegram','whatsapp','grab','foodpanda','robinhood','game','steam','playstore','appstore','url','app'];
function extractWidgetActions(text) {
  const actions = [];
  let clean = text;

  for (const t of OPEN_TAG_TYPES) {
    const re = new RegExp(`<open:${t}>([\\s\\S]*?)<\/open:${t}>`, 'gi');
    clean = clean.replace(re, (_, q) => {
      actions.push({ type: t, query: q.trim() });
      return '';
    });
  }

  // Also catch unclosed / partial tags as fallback
  const typePattern = OPEN_TAG_TYPES.join('|');
  clean = clean.replace(new RegExp(`<open:(${typePattern})[^>]*>`, 'gi'), '');
  clean = clean.replace(new RegExp(`<\/open:(${typePattern})>`, 'gi'), '');

  return { text: clean.trim(), actions };
}

// ── Fire widget actions: create widget + auto-open URL ──
// Safety: reject actions where the query looks like a question (AI hallucinated a tag)
function isQueryLooksLikeQuestion(q) {
  if (!q) return true;
  if (q.trim().length < 2) return true;
  // Contains question mark or common Thai question words at start
  if (/[?？]/.test(q)) return true;
  if (/^(?:อะไร|ที่ไหน|ยังไง|อย่างไร|ทำไม|เมื่อไ|ใคร|ไหน|มั้ย|มาก|หรือ|แล้ว|ดีมั้ย|ดีไหม|what|where|why|how|who|which)\b/i.test(q.trim())) return true;
  return false;
}

