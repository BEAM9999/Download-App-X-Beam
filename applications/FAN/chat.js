// ── Process message for widget commands (called after AI responds) ──
function processWidgetCommands(userText, session) {
  if (!session) return;
  const cmd = detectWidgetCommand(userText);
  if (!cmd) return;

  const keepOld = wantsKeepOld(userText);
  const doReplace = !keepOld; // default: replace existing same-type widget

  // Helper: remove all existing widgets of a given type (cleans up timers/alarms too)
  function clearWidgetsOfType(type) {
    if (!session.widgets) return;
    const toRemove = session.widgets.filter(w => w.type === type);
    for (const w of toRemove) {
      if (alarmIntervals[w.id]) { clearInterval(alarmIntervals[w.id]); delete alarmIntervals[w.id]; }
      if (timerIntervals[w.id]) { clearInterval(timerIntervals[w.id].intervalId); delete timerIntervals[w.id]; }
    }
    session.widgets = session.widgets.filter(w => w.type !== type);
  }

  switch (cmd.type) {
    case 'alarm': {
      if (doReplace) clearWidgetsOfType('alarm');
      const t = cmd.time;
      createAlarmWidget(session, t.hours, t.minutes);
      break;
    }
    case 'timer': {
      if (doReplace) clearWidgetsOfType('timer');
      createTimerWidget(session, cmd.duration);
      break;
    }
    case 'calc': {
      // Calculator: if already open and asked again without keepOld, replace
      if (doReplace) clearWidgetsOfType('calc');
      createCalcWidget(session);
      break;
    }
    case 'map': {
      // Maps are handled via AI response tags — skip from user-message parsing
      break;
    }
    case 'youtube': {
      // YouTube is handled via AI response tags — skip from user-message parsing
      break;
    }
  }
  renderMessages();
}

