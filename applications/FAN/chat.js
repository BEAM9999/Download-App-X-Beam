// ── Thai word → number conversion ──
const THAI_DIGIT_MAP = {
  'ศูนย์':0,'หนึ่ง':1,'สอง':2,'สาม':3,'สี่':4,'ห้า':5,'หก':6,'เจ็ด':7,'แปด':8,'เก้า':9,'สิบ':10,
  'ยี่สิบ':20,'สามสิบ':30,'สี่สิบ':40,'ห้าสิบ':50,
  '๐':0,'๑':1,'๒':2,'๓':3,'๔':4,'๕':5,'๖':6,'๗':7,'๘':8,'๙':9,
};

function thaiToNum(str) {
  if (!str) return NaN;
  str = str.trim();
  // Arabic digits (and Thai digits ๐-๙)
  const normalized = str.replace(/[๐-๙]/g, c => THAI_DIGIT_MAP[c] ?? c);
  if (/^\d+$/.test(normalized)) return parseInt(normalized);
  // Direct Thai word lookup
  for (const [word, val] of Object.entries(THAI_DIGIT_MAP)) {
    if (str === word) return val;
  }
  // Composite: "ยี่สิบห้า" = 25, "สิบสอง" = 12, etc.
  let total = 0;
  const tens = ['ยี่สิบ','สามสิบ','สี่สิบ','ห้าสิบ'];
  const units = ['หนึ่ง','สอง','สาม','สี่','ห้า','หก','เจ็ด','แปด','เก้า'];
  let rem = str;
  for (const t of tens) {
    if (rem.startsWith(t)) { total += THAI_DIGIT_MAP[t]; rem = rem.slice(t.length); break; }
  }
  if (rem.startsWith('สิบ')) {
    if (total === 0) total = 10;
    else total += 10;
    rem = rem.slice(3);
  }
  for (const u of units) {
    if (rem.startsWith(u)) { total += THAI_DIGIT_MAP[u]; rem = rem.slice(u.length); break; }
  }
  return total > 0 ? total : NaN;
}

// ── Extract a number from mixed Thai/Arabic text near a keyword ──
// e.g. "หนึ่งนาที", "2 นาที", "นาทีที่หนึ่ง", "60วิ"
function extractNumberBefore(text, unit) {
  // Try Arabic number directly before unit
  const arabicBefore = text.match(new RegExp('(\\d+(?:\\.\\d+)?)\\s*' + unit, 'u'));
  if (arabicBefore) return parseFloat(arabicBefore[1]);
  // Try Thai word number before unit
  const thaiWords = Object.keys(THAI_DIGIT_MAP).filter(w => /[ก-๙]/.test(w))
    .sort((a,b) => b.length - a.length); // longest first
  for (const w of thaiWords) {
    const idx = text.indexOf(w);
    if (idx !== -1) {
      const after = text.slice(idx + w.length).trimStart();
      if (new RegExp('^' + unit).test(after)) return THAI_DIGIT_MAP[w];
    }
  }
  return NaN;
}

// ── Smart time parser: "14:30", "บ่าย 2 โมง", "2 ทุ่ม", "เที่ยงคืน" etc. ──
function parseSmartTime(text) {
  text = text.toLowerCase();
  // HH:MM or HH.MM
  const hhmm = text.match(/(\d{1,2})[:.ː](\d{2})/);
  if (hhmm) return { hours: +hhmm[1] % 24, minutes: +hhmm[2] };

  // Thai time expressions
  const thaiHour = extractNumberBefore(text, '(?:โมง|นาฬิกา|ชั่วโมง)?');
  const thaiMin  = extractNumberBefore(text, '(?:นาที)');

  // Period prefixes
  let base = 0;
  if (/เช้า|ตี/.test(text))       base = /ตี/.test(text) ? 0 : 6;
  else if (/บ่าย/.test(text))      base = 12;
  else if (/เย็น/.test(text))      base = 15;
  else if (/ทุ่ม/.test(text))      base = 18;
  else if (/เที่ยงคืน/.test(text)) return { hours: 0, minutes: 0 };
  else if (/เที่ยง/.test(text))    return { hours: 12, minutes: 0 };

  if (!isNaN(thaiHour)) {
    let h = thaiHour;
    // "ตี" counts from midnight
    if (/ตี/.test(text)) h = thaiHour % 24;
    else if (base > 0) { h = base + (thaiHour % 12); }
    const m = !isNaN(thaiMin) ? thaiMin : 0;
    return { hours: Math.min(23, h), minutes: Math.min(59, m) };
  }
  return null;
}

// ── Smart duration parser: "1 นาที 2 วิ", "หนึ่งนาทีสองวินาที", "30 วิ", "2 ชั่วโมง" ──
function parseSmartDuration(text) {
  const raw = text;
  text = text.toLowerCase();

  // Replace Thai numbers first so subsequent regex can work on digits
  let normalized = text;
  for (const [word, val] of Object.entries(THAI_DIGIT_MAP).sort((a, b) => b[0].length - a[0].length)) {
    if (/[ก-๙]/.test(word)) normalized = normalized.replace(new RegExp(word, 'g'), ' ' + val + ' ');
  }

  // Pattern: X นาที Y วิ/วินาที (any order)
  let m = normalized.match(/(\d+)\s*(?:นาที|minute|min)\D{0,6}?(\d+)\s*(?:วินาที|วิ|second|sec)/i);
  if (m) return parseInt(m[1]) * 60 + parseInt(m[2]);
  m = normalized.match(/(\d+)\s*(?:วินาที|วิ|second|sec)\D{0,6}?(\d+)\s*(?:นาที|minute|min)/i);
  if (m) return parseInt(m[2]) * 60 + parseInt(m[1]);

  // Pattern: X ชั่วโมง
  m = normalized.match(/(\d+)\s*(?:ชั่วโมง|hour|hr)/);
  if (m) return parseInt(m[1]) * 3600;

  // Stand-alone minutes
  m = normalized.match(/(\d+)\s*(?:นาที|minute|min)/);
  if (m) {
    const mins = parseInt(m[1]);
    // Check for trailing seconds number: e.g. "5 นาที 30" (30 = seconds)
    const afterMin = normalized.slice(normalized.indexOf(m[0]) + m[0].length).trim();
    const trailing = afterMin.match(/^(\d+)(?!\s*(?:นาที|minute|min))/);
    if (trailing && parseInt(trailing[1]) < 60) return mins * 60 + parseInt(trailing[1]);
    return mins * 60;
  }

  // Stand-alone seconds
  m = normalized.match(/(\d+)\s*(?:วินาที|วิ|second|sec)/);
  if (m) {
    const secs = parseInt(m[1]);
    // If there's a trailing number and secs looks like "minutes" (user confused units), treat as M:SS
    const afterSec = normalized.slice(normalized.indexOf(m[0]) + m[0].length).trim();
    const trailing = afterSec.match(/^(\d+)(?!\s*(?:วินาที|วิ|second|sec))/);
    if (trailing && secs <= 59 && parseInt(trailing[1]) <= 59) {
      // "1 วิ 30" → ambiguous: treat as 1 min 30 sec
      return secs * 60 + parseInt(trailing[1]);
    }
    return secs;
  }

  // Bare X:YY (1:30 = 90s)
  m = text.match(/(\d{1,2})[:.：](\d{2})/);
  if (m) return parseInt(m[1]) * 60 + parseInt(m[2]);

  return NaN;
}

