function buildGeminiPayload(session) {
  const sourceMessages = buildMessages(session);
  const systemChunks = [];
  const contents = [];

  for (const msg of sourceMessages) {
    if (msg.role === 'system') {
      systemChunks.push(msg.content);
      continue;
    }
    contents.push({
      role: msg.role === 'assistant' ? 'model' : 'user',
      parts: [{ text: msg.content }]
    });
  }

  const payload = {
    contents: contents.length ? contents : [{ role: 'user', parts: [{ text: 'สวัสดี' }] }],
    generationConfig: {
      maxOutputTokens: 1024,
      temperature: /^gemini-3/.test(getSelectedModelId('gemini')) ? 1 : 0.85,
      topP: 0.95
    }
  };

  if (systemChunks.length) {
    payload.systemInstruction = { parts: [{ text: systemChunks.join('\n\n') }] };
  }
  return payload;
}

function extractGeminiText(json) {
  return (json?.candidates || [])
    .flatMap(candidate => candidate?.content?.parts || [])
    .map(part => part?.text || '')
    .join('')
    .trim();
}

async function readErrorResponse(res) {
  const raw = await res.text();
  let message = raw;
  try {
    const parsed = JSON.parse(raw);
    message = parsed?.error?.message || parsed?.message || raw;
  } catch (_) {}
  return { raw, message };
}

function isGeminiQuotaError(status, message) {
  return status === 429 || /quota|resource_exhausted|rate limit|too many requests|limit exceeded|exceeded your current quota/i.test(String(message || ''));
}

async function checkOpenRouterModelStatus(model, force = false) {
  if (!model) return 'err';
  if (!model.key || !model.modelId) {
    openrouterModelStatus[model.id] = 'err';
    renderModelList();
    return 'err';
  }
  if (!force && openrouterModelStatus[model.id] && openrouterModelStatus[model.id] !== 'loading') return openrouterModelStatus[model.id];

  openrouterModelStatus[model.id] = 'loading';
  renderModelList();
  try {
    const res = await fetch(OPENROUTER_URL, {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${model.key}`,
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        model: model.modelId,
        messages: [{ role: 'user', content: 'test' }],
        max_tokens: 1,
        stream: false
      })
    });
    openrouterModelStatus[model.id] = res.ok ? 'ok' : 'err';
  } catch (_) {
    openrouterModelStatus[model.id] = 'err';
  }
  renderModelList();
  return openrouterModelStatus[model.id];
}

