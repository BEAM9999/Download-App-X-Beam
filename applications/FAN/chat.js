async function checkGeminiModelStatusWithKey(model, slot) {
  markGeminiModelLoading(slot.id, model.id);
  renderModelList();
  try {
    const res = await fetch(`${GEMINI_BASE_URL}/${encodeURIComponent(model.id)}:generateContent?key=${encodeURIComponent(slot.key)}`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        contents: [{ role: 'user', parts: [{ text: 'ok' }] }],
        generationConfig: { maxOutputTokens: 1, temperature: 0 }
      })
    });
    if (res.ok) {
      setGeminiModelRecord(slot.id, model.id, 'ok', { httpStatus: 200 });
      renderModelList();
      return 'ok';
    }
    const err = await readErrorResponse(res);
    setGeminiModelRecord(slot.id, model.id, 'err', { httpStatus: res.status, error: err.message });
  } catch (err) {
    setGeminiModelRecord(slot.id, model.id, 'err', { error: err.message || 'network error' });
  }
  renderModelList();
  return 'err';
}

async function probeGeminiModelAcrossKeys(modelId, opts = {}) {
  const model = getPlatformModels('gemini').find(entry => entry.id === modelId);
  if (!model) return null;
  const excludePairs = opts.excludePairs || new Set();

  for (const slot of orderGeminiKeySlots(opts.preferredKeyId)) {
    const pairKey = `${slot.id}::${model.id}`;
    if (excludePairs.has(pairKey)) continue;

    const record = getGeminiModelRecord(slot.id, model.id);
    if (!opts.force && isGeminiRecordFresh(record)) {
      if (record.status === 'ok') return { modelId: model.id, keySlotId: slot.id, key: slot.key };
      continue;
    }

    const status = await checkGeminiModelStatusWithKey(model, slot);
    if (status === 'ok') return { modelId: model.id, keySlotId: slot.id, key: slot.key };
  }
  return null;
}

async function findGeminiRoute(preferredModelId = getSelectedModelId('gemini'), opts = {}) {
  const excludePairs = opts.excludePairs || new Set();
  const orderedModelIds = [];
  if (preferredModelId) orderedModelIds.push(preferredModelId);
  if (opts.allowOtherModels !== false) {
    for (const model of getPlatformModels('gemini')) {
      if (model.id !== preferredModelId) orderedModelIds.push(model.id);
    }
  }

  for (const modelId of orderedModelIds) {
    const cached = getGeminiRouteFromCache(modelId, excludePairs);
    if (cached) return cached;
    const probed = await probeGeminiModelAcrossKeys(modelId, opts);
    if (probed) return probed;
  }
  return null;
}

function applyGeminiRoute(route, opts = {}) {
  if (!route) return false;
  geminiActiveKeyId = route.keySlotId;
  setSelectedModelForPlatform('gemini', route.modelId);
  save();
  saveGlobal();
  updatePlatformUI();
  if (!opts.skipRender) renderModelList();
  return true;
}

async function ensureGeminiModelStatuses(force = false) {
  if (!getPlatformModels('gemini').length || !getGeminiKeySlots().length) return;
  if (geminiScanPromise && !force) return geminiScanPromise;

  const needsCheck = force || getPlatformModels('gemini').some(model => !hasGeminiFreshData(model.id));
  if (!needsCheck) {
    const cached = await findGeminiRoute(getSelectedModelId('gemini'), { allowOtherModels: true });
    if (cached) applyGeminiRoute(cached, { skipRender: true });
    renderModelList();
    return;
  }

  geminiScanPromise = (async () => {
    for (const model of getPlatformModels('gemini')) {
      await probeGeminiModelAcrossKeys(model.id, { force });
      renderModelList();
    }
    const best = await findGeminiRoute(getSelectedModelId('gemini'), { allowOtherModels: true });
    if (best) applyGeminiRoute(best, { skipRender: true });
  })().finally(() => {
    geminiScanPromise = null;
    updatePlatformUI();
    renderModelList();
  });
  return geminiScanPromise;
}

function ensureCurrentPlatformStatuses(force = false) {
  if (selectedPlatform === 'gemini') ensureGeminiModelStatuses(force);
  else ensureOpenRouterModelStatuses(force);
}

async function requestOpenRouterResponse(session) {
  const messages = buildMessages(session);
  const excludeIds = new Set();
  let lastError = null;

  for (const model of getOrderedOpenRouterModels(getSelectedModelId('openrouter'), excludeIds)) {
    if (!model.key || !model.modelId) {
      openrouterModelStatus[model.id] = 'err';
      continue;
    }

    applyOpenRouterRoute(model, { skipRender: true });

    const res = await fetch(OPENROUTER_URL, {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${model.key}`,
        'Content-Type': 'application/json',
        'HTTP-Referer': location.href,
        'X-Title': 'FAN Chat'
      },
      body: JSON.stringify({
        model: model.modelId,
        messages,
        stream: true,
        max_tokens: 1024,
        temperature: 0.75,
        top_p: 0.9,
        frequency_penalty: 0.7,
        repetition_penalty: 1.15
      }),
      signal: abortCtrl.signal
    });

    if (!res.ok) {
      openrouterModelStatus[model.id] = 'err';
      renderModelList();
      const err = await readErrorResponse(res);
      lastError = new Error(`OpenRouter ${res.status}: ${err.message}`);
      excludeIds.add(model.id);
      if (isOpenRouterRetryableError(res.status, err.message)) continue;
      throw lastError;
    }

    openrouterModelStatus[model.id] = 'ok';
    renderModelList();

    const reader = res.body.getReader();
    const decoder = new TextDecoder();
    let buffer = '';
    let fullText = '';

    while (true) {
      const { done, value } = await reader.read();
      if (done) break;
      buffer += decoder.decode(value, { stream: true });
      const lines = buffer.split('\n');
      buffer = lines.pop() || '';

      for (const line of lines) {
        const trimmed = line.trim();
        if (!trimmed || !trimmed.startsWith('data: ')) continue;
        const data = trimmed.slice(6);
        if (data === '[DONE]') continue;
        try {
          const json = JSON.parse(data);
          const token = json.choices?.[0]?.delta?.content;
          if (token) {
            fullText += token;
            appendBotChunk(fullText);
          }
        } catch (_) {}
      }
    }

    return fullText;
  }

  throw lastError || new Error('ยังไม่มี OpenRouter model/API key ที่พร้อมใช้งาน');
}

async function requestGeminiResponse(session, triedPairs = new Set()) {
  if (!getGeminiKeySlots().length) {
    throw new Error('ยังไม่ได้กรอก Gemini API Key');
  }
  const preferredModelId = getSelectedModelId('gemini');
  let route = getGeminiRouteFromCache(preferredModelId, triedPairs);
  if (!route) route = await findGeminiRoute(preferredModelId, { allowOtherModels: true, excludePairs: triedPairs });
  if (!route) throw new Error('Gemini ทุกโมเดลและทุก API key ไม่มีโควต้าในตอนนี้');

  applyGeminiRoute(route, { skipRender: true });

  const res = await fetch(`${GEMINI_BASE_URL}/${encodeURIComponent(route.modelId)}:generateContent?key=${encodeURIComponent(route.key)}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(buildGeminiPayload(session)),
    signal: abortCtrl.signal
  });

  if (!res.ok) {
    const err = await readErrorResponse(res);
    setGeminiModelRecord(route.keySlotId, route.modelId, 'err', { httpStatus: res.status, error: err.message });
    renderModelList();

    const nextTried = new Set(triedPairs);
    nextTried.add(`${route.keySlotId}::${route.modelId}`);

    if (isGeminiQuotaError(res.status, err.message)) {
      const fallback = await findGeminiRoute(route.modelId, { allowOtherModels: true, excludePairs: nextTried });
      if (fallback) {
        applyGeminiRoute(fallback, { skipRender: true });
        return requestGeminiResponse(session, nextTried);
      }
    }

    throw new Error(`Gemini ${res.status}: ${err.message}`);
  }

  setGeminiModelRecord(route.keySlotId, route.modelId, 'ok', { httpStatus: 200 });
  geminiActiveKeyId = route.keySlotId;
  saveGlobal();
  renderModelList();

  const json = await res.json();
  return extractGeminiText(json);
}

async function sendToAI(session, retryCount = 0) {
  if (isGenerating && retryCount === 0) return;
  isGenerating = true;
  abortCtrl = new AbortController();
  $('typingIndicator').classList.add('active');
  updateSendBtn();

  try {
    // Check if user wants image generation (Imagen 4) — run for ALL platforms
    const lastUserMsg = session.messages.filter(m => m.role === 'user').pop();
    if (lastUserMsg && getGeminiKeySlots().length) {
      const imgDone = await tryImageGeneration(lastUserMsg.content, session);
      if (imgDone) return;
    }

    const fullText = selectedPlatform === 'gemini'
      ? await requestGeminiResponse(session)
      : await requestOpenRouterResponse(session);

    if (fullText) {
      // Extract AI-embedded widget action tags before storing/displaying
      const { text: cleanText, actions } = extractWidgetActions(fullText);
      const botMessage = { role: 'assistant', content: cleanText || fullText, ts: Date.now() };
      session.messages.push(botMessage);
      finalizeBotMsg(botMessage.content, botMessage.ts);
      sfxAiDone();
      save();
      // Fire AI-generated widget actions (youtube/maps auto-open)
      if (actions.length) fireWidgetActions(actions, session);
      // Process alarm/timer/calc from user message only
      if (lastUserMsg) processWidgetCommands(lastUserMsg.content, session);
      // Send notification if tab not focused
      sendNotification(botMessage.content);
    } else if (retryCount < 2) {
      isGenerating = false;
      abortCtrl = null;
      $('typingIndicator').classList.remove('active');
      updateSendBtn();
      setTimeout(() => sendToAI(session, retryCount + 1), 1000);
      return;
    }
  } catch (err) {
    if (err.name === 'AbortError') return;
    const errMsg = '❌ ขอโทษนะ เกิดข้อผิดพลาด: ' + err.message;
    session.messages.push({ role: 'assistant', content: errMsg, ts: Date.now() });
    renderMessages();
    save();
  } finally {
    isGenerating = false;
    abortCtrl = null;
    $('typingIndicator').classList.remove('active');
    updateSendBtn();
  }
}

