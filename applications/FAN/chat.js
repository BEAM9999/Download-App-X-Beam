function renderModelList() {
  const el = $('modelList');
  if (!el) return;

  updatePlatformUI();
  const platformId = selectedPlatform;
  const models = getPlatformModels(platformId);
  const currentModelId = getSelectedModelId(platformId);

  if (!models.length) {
    el.innerHTML = '<div class="model-note">ยังไม่มีโมเดลสำหรับแพลตฟอร์มนี้</div>';
    return;
  }

  el.innerHTML = models.map(m => {
    const st = getModelStatus(m, platformId);
    const active = m.id === currentModelId ? 'active' : '';
    const nameClass = st === 'err' ? 'quota' : '';
    let sub = m.note || '';
    if (st === 'loading') sub = sub ? `${sub} • กำลังตรวจสอบ` : 'กำลังตรวจสอบ';
    if (st === 'ok') sub = sub ? `${sub} • พร้อมใช้งาน` : 'พร้อมใช้งาน';
    if (st === 'err') {
      const reason = platformId === 'gemini'
        ? (getGeminiKeySlots().length ? 'ไม่มีโควต้า' : 'ยังไม่มี API key')
        : (m.key ? 'ไม่มีโควต้า' : 'ยังไม่มี API key');
      sub = sub ? `${sub} • ${reason}` : reason;
    }
    const subClass = st === 'err' ? 'quota' : '';
    return `
      <div class="model-item ${active}" onclick='selectModel(${JSON.stringify(m.id)})'>
        <span class="model-dot ${st}"></span>
        <div class="model-item-text">
          <span class="model-item-name ${nameClass}">${esc(m.name)}</span>
          <span class="model-item-sub ${subClass}">${esc(sub)}</span>
        </div>
      </div>`;
  }).join('');
}

