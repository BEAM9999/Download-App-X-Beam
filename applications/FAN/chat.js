async function importPlatformConfigText(text, platformId = selectedPlatform) {
  const normalizedText = String(text || '').trim();
  if (!normalizedText) throw new Error('ยังไม่มีข้อความสำหรับนำเข้า');

  if (platformId === 'gemini') {
    const imported = parseGeminiConfigText(normalizedText);
    if (!imported.length) throw new Error('ไม่พบ Gemini API key ในข้อมูลที่นำเข้า');
    geminiApiKeys = [...getValidGeminiKeyEntries(), ...imported];
    cleanupGeminiKeyEntries();
    resetGeminiQuotaState();
    syncPlatformSelectionState();
    save();
    saveGlobal();
    renderPlatformConfig();
    renderPlatformList();
    renderModelList();
    ensureGeminiModelStatuses(true);
    return;
  }

  const imported = parseOpenRouterConfigText(normalizedText);
  if (!imported.length) throw new Error('ไม่พบข้อมูล OpenRouter ที่มี model ID หรือ API key ในข้อมูลที่นำเข้า');
  const meaningfulExisting = openrouterEntries.filter(entry => entry.name || entry.modelId || entry.apiKey);
  openrouterEntries = [...meaningfulExisting, ...imported];
  cleanupOpenRouterEntries();
  reconcileOpenRouterStatusMap();
  syncPlatformSelectionState();
  save();
  saveGlobal();
  renderPlatformConfig();
  renderPlatformList();
  renderModelList();
  ensureOpenRouterModelStatuses(true);
}

