// Convert raw PCM (L16) base64 to playable WAV Blob
function pcmToWav(pcmBase64, sampleRate) {
  sampleRate = sampleRate || 24000;
  const raw = atob(pcmBase64);
  const pcmBytes = new Uint8Array(raw.length);
  for (let i = 0; i < raw.length; i++) pcmBytes[i] = raw.charCodeAt(i);
  const numCh = 1, bps = 16;
  const byteRate = sampleRate * numCh * bps / 8;
  const blockAlign = numCh * bps / 8;
  const dataSize = pcmBytes.length;
  const buf = new ArrayBuffer(44 + dataSize);
  const v = new DataView(buf);
  const ws = (off, s) => { for (let i = 0; i < s.length; i++) v.setUint8(off + i, s.charCodeAt(i)); };
  ws(0,'RIFF'); v.setUint32(4, 36 + dataSize, true);
  ws(8,'WAVE'); ws(12,'fmt ');
  v.setUint32(16, 16, true); v.setUint16(20, 1, true);
  v.setUint16(22, numCh, true); v.setUint32(24, sampleRate, true);
  v.setUint32(28, byteRate, true); v.setUint16(32, blockAlign, true);
  v.setUint16(34, bps, true); ws(36,'data'); v.setUint32(40, dataSize, true);
  new Uint8Array(buf).set(pcmBytes, 44);
  return new Blob([buf], { type: 'audio/wav' });
}

// Build audio Blob from Gemini inlineData — handles PCM L16 and normal formats
function buildAudioBlob(inlineData) {
  const mimeType = inlineData.mimeType || '';
  const data = inlineData.data;
  if (/L16|pcm|raw/i.test(mimeType)) {
    const rateMatch = mimeType.match(/rate=(\d+)/);
    return pcmToWav(data, rateMatch ? parseInt(rateMatch[1]) : 24000);
  }
  return new Blob(
    [Uint8Array.from(atob(data), c => c.charCodeAt(0))],
    { type: mimeType || 'audio/wav' }
  );
}

async function speakMsg(btn) {
  sfxBtn();
  const text = btn.getAttribute('data-text');
  if (!text) return;

  // ── Browser TTS — free, Web Speech API (Microsoft Neural voices in Edge/Chrome) ──
  if (ttsPlatform === 'browser') {
    if (!window.speechSynthesis) { alert('เบราว์เซอร์นี้ไม่รองรับ Speech Synthesis — ลอง Edge หรือ Chrome'); return; }
    if (speechSynthesis.speaking) {
      speechSynthesis.cancel();
      btn.textContent = '🔊 ฟัง';
      return;
    }
    const utter = new SpeechSynthesisUtterance(text);
    const allVoices = speechSynthesis.getVoices();
    const chosenVoice = allVoices.find(vx => vx.name === ttsBrowserVoiceName);
    if (chosenVoice) utter.voice = chosenVoice;
    else utter.lang = 'th-TH';
    utter.onend = () => { btn.textContent = '🔊 ฟัง'; };
    utter.onerror = () => { btn.textContent = '🔊 ฟัง'; };
    btn.textContent = '⏸️ หยุด';
    speechSynthesis.speak(utter);
    return;
  }

  // ── Gemini TTS ──────────────────────────────────────────
  // If already playing, stop
  if (_ttsAudio && !_ttsAudio.paused) {
    _ttsAudio.pause();
    _ttsAudio = null;
    btn.textContent = '🔊 ฟัง';
    return;
  }

  const keySlots = getGeminiKeySlots();
  if (!keySlots.length) { alert('กรุณาเพิ่ม Gemini API Key ก่อน'); return; }

  const ttsModels = getGeminiSpecialModels('tts');
  const ttsModelId = ttsModels.length ? ttsModels[0].id : 'gemini-2.5-flash-preview-tts';
  const voiceName = ttsVoiceName || defaultVoice();

  btn.textContent = '⏳ กำลังสร้างเสียง...';

  for (const slot of keySlots) {
    try {
      const res = await fetch(
        `${GEMINI_BASE_URL}/${encodeURIComponent(ttsModelId)}:generateContent?key=${encodeURIComponent(slot.key)}`,
        {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            contents: [{ parts: [{ text }] }],
            generationConfig: {
              responseModalities: ['AUDIO'],
              speechConfig: { voiceConfig: { prebuiltVoiceConfig: { voiceName } } }
            }
          })
        }
      );
      if (!res.ok) {
        if (res.status === 429 || res.status === 403) continue;
        console.warn('[TTS] API error', res.status, await res.text().catch(() => ''));
        continue;
      }
      const json = await res.json();
      const audioPart = json?.candidates?.[0]?.content?.parts?.find(p => p.inlineData);
      if (!audioPart) { console.warn('[TTS] No inlineData in response', json); continue; }

      const audioBlob = buildAudioBlob(audioPart.inlineData);
      const audioUrl = URL.createObjectURL(audioBlob);
      _ttsAudio = new Audio(audioUrl);
      _ttsAudio.onerror = () => {
        console.warn('[TTS] Audio playback error');
        btn.textContent = '🔊 ฟัง';
        URL.revokeObjectURL(audioUrl);
        _ttsAudio = null;
      };
      _ttsAudio.onended = () => {
        btn.textContent = '🔊 ฟัง';
        URL.revokeObjectURL(audioUrl);
        _ttsAudio = null;
      };
      btn.textContent = '⏸️ หยุด';
      await _ttsAudio.play();
      return;
    } catch (e) {
      if (e.name === 'AbortError') return;
      console.warn('[TTS] Exception for key slot', slot.index, e.message);
      continue;
    }
  }
  btn.textContent = '🔊 ฟัง';
  alert('ไม่สามารถสร้างเสียงได้ — ลองตรวจสอบ API Key หรือลองใหม่ภายหลัง');
}

// ─── 22. Imagen 4 — Image Generation ─────────────────────
async function requestImagenGeneration(prompt) {
  const keySlots = getGeminiKeySlots();
  if (!keySlots.length) throw new Error('กรุณาเพิ่ม Gemini API Key ก่อน');

  const imageModels = getGeminiSpecialModels('image');
  const modelOrder = imageModels.length
    ? imageModels.map(m => m.id)
    : ['imagen-4.0-flash-preview', 'imagen-4.0-generate-preview', 'imagen-4.0-ultra-generate-preview'];

  for (const modelId of modelOrder) {
    for (const slot of keySlots) {
      try {
        const res = await fetch(
          `${GEMINI_BASE_URL}/${encodeURIComponent(modelId)}:generateImages?key=${encodeURIComponent(slot.key)}`,
          {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
              model: `models/${modelId}`,
              prompt: { text: prompt },
              config: { numberOfImages: 1, outputOptions: { mimeType: 'image/png' } }
            })
          }
        );
        if (!res.ok) {
          if (res.status === 429 || res.status === 403) continue;
          continue;
        }
        const json = await res.json();
        const img = json?.generatedImages?.[0]?.image;
        if (!img) continue;
        return `data:${img.mimeType || 'image/png'};base64,${img.bytesBase64Encoded}`;
      } catch (e) { continue; }
    }
  }
  return null;
}

// Detect image request in user message and generate if needed
async function tryImageGeneration(userText, session) {
  // Strict regex — only trigger on clear image generation requests
  const imgRegex = /(?:สร้างรูป|วาดรูป|ช่วยวาด|generate\s+(?:an?\s+)?image|draw\s+(?:a\s+|an?\s+)?(?:picture|image|photo|\S+)|ขอรูป|ทำรูป|สร้างภาพ|วาดภาพ|ขอภาพ|วาดให้|สร้างให้.*รูป|วาด[ๆ\s]*(?:หนู|แมว|หมา|สัตว์|คน|ดอกไม้|ภาพ|รูป))/iu;
  if (!imgRegex.test(userText)) return false;
  if (!getGeminiKeySlots().length) return false;

  appendBotChunk('🎨 กำลังสร้างรูปภาพ...');

  try {
    // Use the text after the trigger word as prompt
    const prompt = userText.replace(imgRegex, '').trim() || userText;
    const dataUrl = await requestImagenGeneration(prompt);
    if (dataUrl) {
      const content = `นี่เลยค่ะ/ครับ รูปที่สร้างให้ 🎨\n\n![Generated Image](${dataUrl})`;
      const botMsg = { role: 'assistant', content, ts: Date.now() };
      session.messages.push(botMsg);
      finalizeBotMsg(content, botMsg.ts);
      sfxAiDone();
      save();
      return true;
    }
  } catch (_) {}
  return false;
}

