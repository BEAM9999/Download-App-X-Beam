function calcPress(widgetId, val) {
  const session = activeSession();
  if (!session) return;
  const w = session.widgets?.find(w => w.id === widgetId);
  if (!w) return;
  if (val === 'C') { w.data.expression = ''; w.data.result = ''; }
  else if (val === '⌫') { w.data.expression = w.data.expression.slice(0, -1); }
  else if (val === '=') {
    try {
      // Safe eval: only allow numbers and basic operators
      const expr = w.data.expression.replace(/[^0-9+\-*/().%]/g, '');
      if (expr) w.data.result = String(Function('"use strict"; return (' + expr + ')')());
      else w.data.result = '';
    } catch (_) { w.data.result = 'Error'; }
  } else {
    w.data.expression += val;
  }
  save();
  const dispEl = document.querySelector(`[data-widget-id="${widgetId}"] .calc-expression`);
  const resEl = document.querySelector(`[data-widget-id="${widgetId}"] .calc-result`);
  if (dispEl) dispEl.textContent = w.data.expression || '0';
  if (resEl) resEl.textContent = w.data.result || '';
}

