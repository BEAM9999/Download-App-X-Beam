function splitTextBlocks(text) {
  return String(text || '')
    .replace(/\r\n?/g, '\n')
    .replace(/^-{3,}[- \t]*$/gm, '')   // strip ----... separator lines
    .split(/\n\s*\n+/)
    .map(block => block.trim())
    .filter(Boolean);
}

function parseKeyValueBlock(block) {
  const result = {};
  for (const rawLine of block.split('\n')) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#') || line.startsWith('//') || line.startsWith(';')) continue;
    const match = line.match(/^([a-zA-Z0-9_-]+)\s*[:=]\s*(.+)$/);
    if (!match) continue;
    result[match[1].toLowerCase()] = match[2].trim();
  }
  return result;
}

function parseGeminiConfigText(text) {
  const blocks = splitTextBlocks(text);
  const entries = [];

  for (const block of blocks) {
    const parsed = parseKeyValueBlock(block);
    const rawKey = parsed.key || parsed.apikey || parsed.api_key || parsed.value || '';
    if (rawKey) {
      entries.push(createGeminiKeyEntry({ key: rawKey }));
      continue;
    }
    for (const rawLine of block.split('\n')) {
      const line = rawLine.trim();
      if (!line || line.startsWith('#') || line.startsWith('//') || line.startsWith(';')) continue;
      if (/^AIza[0-9A-Za-z_-]{10,}$/.test(line)) entries.push(createGeminiKeyEntry({ key: line }));
    }
  }

  return entries.filter(entry => entry.key);
}

function parseOpenRouterConfigText(text) {
  const blocks = splitTextBlocks(text);
  const entries = [];

  for (const block of blocks) {
    const parsed = parseKeyValueBlock(block);
    const modelId = parsed.model || parsed.modelid || parsed.id || '';
    const apiKey = parsed.key || parsed.apikey || parsed.api_key || '';
    const name = parsed.name || '';
    if (modelId || apiKey || name) {
      entries.push(createOpenRouterEntry({ name, modelId, apiKey }));
    }
  }

  return entries.filter(entry => entry.modelId || entry.apiKey || entry.name);
}

