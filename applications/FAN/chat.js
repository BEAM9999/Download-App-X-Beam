// ─── 7. Markdown Renderer ────────────────────────────────
function renderMd(text) {
  if (!text) return '';
  let html = esc(text);

  // Code blocks
  html = html.replace(/```(\w*)\n?([\s\S]*?)```/g, (_, lang, code) =>
    `<pre><code class="lang-${lang}">${code.trim()}</code></pre>`);
  // Inline code
  html = html.replace(/`([^`]+)`/g, '<code>$1</code>');
  // Bold
  html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  // Italic
  html = html.replace(/\*(.+?)\*/g, '<em>$1</em>');
  // Strikethrough
  html = html.replace(/~~(.+?)~~/g, '<s>$1</s>');
  // Images — inline Markdown
  html = html.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, (_, alt, src) =>
    `<span class="img-wrap"><img src="${src}" alt="${esc(alt)}" loading="lazy" onclick="previewImg(this.src)"><button class="img-menu-btn" onclick="previewImg('${src}')">⋮</button></span>`);
  // Images — reference-style  [label]: url  (AI sometimes sends this)
  html = html.replace(/^\[([^\]]+)\]:\s+(https?:\/\/[^\s&]+(?:&amp;[^\s]*)*)/gm, (_, label, src) => {
    src = src.replace(/&amp;/g, '&');
    if (/\.(jpe?g|png|gif|webp|svg|bmp)(\?|$)/i.test(src) || /tenor\.com|giphy\.com|freepik|imgur|googleusercontent/i.test(src))
      return `<span class="img-wrap"><img src="${src}" alt="${esc(label)}" loading="lazy" onclick="previewImg(this.src)"><button class="img-menu-btn" onclick="previewImg('${src}')">⋮</button></span>`;
    return `<a href="${src}" target="_blank" rel="noopener">${label}</a>`;
  });
  // Links
  html = html.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" target="_blank" rel="noopener">$1</a>');
  // Bare image URLs not yet wrapped  (http(s)://...jpg or tenor/giphy)
  html = html.replace(/(^|[\s>])(?!<)(https?:\/\/[^\s<"]+\.(?:jpe?g|png|gif|webp|svg)(?:\?[^\s<"]*)?)(?=[\s<]|$)/gim, (_, pre, url) =>
    `${pre}<span class="img-wrap"><img src="${url}" alt="" loading="lazy" onclick="previewImg(this.src)"><button class="img-menu-btn" onclick="previewImg('${url}')">⋮</button></span>`);
  html = html.replace(/(^|[\s>])(?!<)(https?:\/\/(?:media\.tenor\.com|media\d?\.giphy\.com)\/[^\s<"]+)(?=[\s<]|$)/gim, (_, pre, url) =>
    `${pre}<span class="img-wrap"><img src="${url}" alt="" loading="lazy" onclick="previewImg(this.src)"><button class="img-menu-btn" onclick="previewImg('${url}')">⋮</button></span>`);
  // Blockquote
  html = html.replace(/^&gt; ?(.+)$/gm, '<blockquote>$1</blockquote>');
  // Horizontal rule
  html = html.replace(/^---$/gm, '<hr>');
  // Tables
  html = renderTables(html);
  // Unordered list
  html = html.replace(/^[\-\*] (.+)$/gm, '<li>$1</li>');
  html = html.replace(/(<li>.*<\/li>(\n|$))+/g, m => `<ul>${m}</ul>`);
  // Ordered list
  html = html.replace(/^\d+\. (.+)$/gm, '<li>$1</li>');
  // Paragraphs
  html = html.replace(/\n{2,}/g, '</p><p>');
  html = html.replace(/\n/g, '<br>');
  html = `<p>${html}</p>`;
  html = html.replace(/<p><\/p>/g, '');

  return html;
}

function renderTables(html) {
  return html.replace(/((?:\|.+\|\n?)+)/g, block => {
    const rows = block.trim().split('\n').filter(r => r.trim());
    if (rows.length < 2) return block;
    // Check for separator row
    const sepIdx = rows.findIndex(r => /^\|[\s\-:|]+\|$/.test(r.trim()));
    let thead = '', tbody = '';
    const parseRow = (r, tag) => {
      const cells = r.split('|').filter((_,i,a) => i > 0 && i < a.length - 1);
      return `<tr>${cells.map(c => `<${tag}>${c.trim()}</${tag}>`).join('')}</tr>`;
    };
    if (sepIdx === 1) {
      thead = `<thead>${parseRow(rows[0], 'th')}</thead>`;
      tbody = `<tbody>${rows.slice(2).map(r => parseRow(r, 'td')).join('')}</tbody>`;
    } else {
      tbody = `<tbody>${rows.map(r => parseRow(r, 'td')).join('')}</tbody>`;
    }
    return `<table>${thead}${tbody}</table>`;
  });
}

