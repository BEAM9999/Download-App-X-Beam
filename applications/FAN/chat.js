async function processVoiceChatAudio(audioBlob) {
  const keySlots = getGeminiKeySlots();
  if (!keySlots.length) return;

  const pe = PERSONAS[persona];
  const voiceModels = getGeminiSpecialModels('voice');
  const voiceModelId = voiceModels.length ? voiceModels[0].id : 'gemini-2.5-flash-preview-native-audio-dialog';
  const voiceName = ttsVoiceName || defaultVoice();

  const reader = new FileReader();
  const base64 = await new Promise((resolve) => {
    reader.onloadend = () => resolve(reader.result.split(',')[1]);
    reader.readAsDataURL(audioBlob);
  });

  const mimeType = audioBlob.type || 'audio/webm';
  const systemText = resolvePersonaPrompt(pe, persona) + '\n\nตอบเป็นภาษาไทย สั้นกระชับเหมือนคุยกันปกติ อย่ายาวเกินไป';

  for (const slot of keySlots) {
    try {
      const res = await fetch(
        `${GEMINI_BASE_URL}/${encodeURIComponent(voiceModelId)}:generateContent?key=${encodeURIComponent(slot.key)}`,
        {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            systemInstruction: { parts: [{ text: systemText }] },
            contents: [{ parts: [{ inlineData: { mimeType, data: base64 } }] }],
            generationConfig: {
              responseModalities: ['AUDIO'],
              speechConfig: { voiceConfig: { prebuiltVoiceConfig: { voiceName } } }
            }
          })
        }
      );

      if (!res.ok) {
        if (res.status === 429 || res.status === 403) continue;
        continue;
      }

      const json = await res.json();
      const audioPart = json?.candidates?.[0]?.content?.parts?.find(p => p.inlineData);
      if (!audioPart) { console.warn('[VoiceChat] No inlineData', json); continue; }

      const audioResBlob = buildAudioBlob(audioPart.inlineData);
      const audioUrl = URL.createObjectURL(audioResBlob);

      const vcStatus = $('vcStatus');
      if (vcStatus) vcStatus.textContent = `${pe.name} กำลังพูด...`;

      const audio = new Audio(audioUrl);
      audio.onerror = () => {
        console.warn('[VoiceChat] Audio playback error');
        if (vcStatus) vcStatus.textContent = '❌ เล่นเสียงไม่ได้ ลองใหม่';
        URL.revokeObjectURL(audioUrl);
      };
      audio.onended = () => {
        if (vcStatus) vcStatus.textContent = 'กดปุ่มไมค์เพื่อพูดต่อ';
        URL.revokeObjectURL(audioUrl);
      };
      await audio.play();
      return;
    } catch (e) { continue; }
  }

  const vcStatus = $('vcStatus');
  if (vcStatus) vcStatus.textContent = '❌ โควต้าหมดทุก key ลองใหม่ภายหลัง';
}

