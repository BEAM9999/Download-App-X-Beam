function startAlarmCheck(widget) {
  if (!widget || !widget.data.enabled) return;
  if (alarmIntervals[widget.id]) clearInterval(alarmIntervals[widget.id]);
  alarmIntervals[widget.id] = setInterval(() => {
    const now = getThaiTime();
    if (now.getHours() === widget.data.hours && now.getMinutes() === widget.data.minutes) {
      triggerAlarm(widget);
    }
  }, 30000); // Check every 30 seconds
}

function triggerAlarm(widget) {
  if (!widget.data.enabled) return;
  widget.data.enabled = false; // One-shot alarm
  clearInterval(alarmIntervals[widget.id]);
  delete alarmIntervals[widget.id];
  save();
  playAlarmSound();
  showAlarmNotification(widget);
  renderMessages(); // Re-render to update widget UI
}

function playAlarmSound() {
  try {
    const ctx = new (window.AudioContext || window.webkitAudioContext)();
    const playTone = (freq, startTime, duration) => {
      const osc = ctx.createOscillator();
      const gain = ctx.createGain();
      osc.connect(gain); gain.connect(ctx.destination);
      osc.type = 'sine'; osc.frequency.value = freq;
      gain.gain.setValueAtTime(0.3, startTime);
      gain.gain.exponentialRampToValueAtTime(0.01, startTime + duration);
      osc.start(startTime); osc.stop(startTime + duration);
    };
    // Cheerful alarm melody
    const t = ctx.currentTime;
    [523, 659, 784, 1047, 784, 1047].forEach((f, i) => playTone(f, t + i * 0.2, 0.18));
    setTimeout(() => {
      [523, 659, 784, 1047, 784, 1047].forEach((f, i) => playTone(f, ctx.currentTime + i * 0.2, 0.18));
    }, 1500);
    setTimeout(() => ctx.close(), 4000);
  } catch (_) {}
}

